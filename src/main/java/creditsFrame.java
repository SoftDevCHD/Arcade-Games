    public static void creditsFrame() { //This is the frame that contains the credits
        JFrame credits = new JFrame("Credits"); //Creates frame
        credits.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        
        basic g = new basic();  //Creates background
        //Default button and label dimensions
        int Dhori = 110;
        int Dvert = 30;
        int Lhori = 400;
        int Lvert = 50;
        
        JButton back = new JButton("Back"); //Creates button
        back.addActionListener(new ActionListener() {   //Adds action listener to button
            public void actionPerformed(ActionEvent e) {
                menuFrame.getMenu().setVisible(true);
                credits.dispose();
            }
        });
        back.setToolTipText("Return to the main menu");
        
        //Creates labels
        JLabel title = new JLabel("Credits");
        JLabel cn = new JLabel("Corey Nambu, Cgnambu");
        JLabel cd = new JLabel("Clarence Domond, Cdomond");
        JLabel ms = new JLabel("Michael Surma, Masurma");
        JLabel jc = new JLabel("Joshua Cervantes, Jecervantes");
        
        //Sets fonts for labels
        title.setFont(new Font("Verdana", Font.BOLD, 18));
        cd.setFont(new Font("Verdana", Font.BOLD, 12));
        cn.setFont(new Font("Verdana", Font.BOLD, 12));
        ms.setFont(new Font("Verdana", Font.BOLD, 12));
        jc.setFont(new Font("Verdana", Font.BOLD, 12));
        
        //Set font colors
        title.setForeground(Color.BLACK);
        cn.setForeground(Color.BLACK);
        cd.setForeground(Color.BLACK);
        ms.setForeground(Color.BLACK);
        jc.setForeground(Color.BLACK);
        
        //Adds button and labels
        credits.add(back);
        credits.add(title);
        credits.add(cn);
        credits.add(cd);
        credits.add(ms);
        credits.add(jc);
        
        //Determinse the size and position of the button and labels
        back.setBounds(30, 350, Dhori, Dvert);
        title.setBounds(250, 10, Dhori, Dvert);
        cn.setBounds(200, 100, Lhori, Lvert);
        cd.setBounds(200, 150, Lhori, Lvert);
        ms.setBounds(200, 200, Lhori, Lvert);
        jc.setBounds(200, 250, Lhori, Lvert);
        
        //Create killbind keybind
        credits.getRootPane().getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), "EXIT");
        credits.getRootPane().getActionMap().put("EXIT", new AbstractAction() {
            public void actionPerformed(ActionEvent e) {
            credits.dispose();
            System.exit(0);
            }
        });
        
        //KeyBindings: Popup
        credits.getRootPane().getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke("F1"), "POPUP");
        credits.getRootPane().getActionMap().put("POPUP", new AbstractAction() {
            public void actionPerformed(ActionEvent e) {
                popup.popWindow();
            }
        });
        
        //Draws background on frame and makes it visible
        credits.add(g);
        credits.pack();
        credits.setVisible(true);
        credits.setLocationRelativeTo(null);
    }
